    /**
     * Captura la fecha del `JDateChooser` y la convierte a `String`.
     *
     * @return Fecha en formato `yyyy-MM-dd` o `null` si no está seleccionada.
     */
    private String obtenerFechaInstalacion() {
        Date fecha = jDateChooser_fechaInstalacion.getDate();
        if (fecha != null) {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            return sdf.format(fecha);
        }
        return null;
    }

    /**
     * Configura el modelo para la tabla de clientes.
     */
    private void inicializarModeloTabla() {
        modeloTabla = new DefaultTableModel(
                new String[]{"ID", "Nombre", "DNI", "Dirección", "Teléfono", "Medidor", "Estado", "Fecha Instalación"},
                0
        );
        jTable_paciente.setModel(modeloTabla);
    }

    /**
     * Carga los clientes desde la base de datos en la tabla.
     */
    private void cargarClientesEnTabla() {
        modeloTabla.setRowCount(0); // Limpiar la tabla
        ClienteLogica clienteLogica = new ClienteLogica();
        List<Cliente> clientes = clienteLogica.obtenerClientes();

        for (Cliente cliente : clientes) {
            modeloTabla.addRow(new Object[]{
                    cliente.getIdUsuario(),
                    cliente.getNombre(),
                    cliente.getDni(),
                    cliente.getDireccion(),
                    cliente.getTelefono(),
                    cliente.getTieneMedidor(),
                    cliente.getEstadoServicio(),
                    cliente.getFechaInstalacion()
            });
        }
    }

