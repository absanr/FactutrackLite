    /**
     * Acción del botón "Registrar".
     *
     * @param evt Evento del botón.
     */
    private void jButton_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_registrarActionPerformed
        // Capturar datos del formulario
        String nombre = jTextField_nombre.getText().trim();
        String dni = jTextField_dni.getText().trim();
        String direccion = jTextField_direccion.getText().trim();
        String telefono = jTextField_telefono.getText().trim();
        String fechaInstalacion = obtenerFechaInstalacion();
        String tieneMedidor = jRadioButton_medidor_option_si.isSelected() ? "Sí" : "No";
        String estadoServicio = jRadioButton_estadoServicio_option_activo.isSelected() ? "Activo" : "Suspendido";

        if (nombre.isEmpty() || dni.isEmpty() || fechaInstalacion == null) {
            JOptionPane.showMessageDialog(this, "Por favor complete los campos obligatorios.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Cliente cliente = new Cliente();
        cliente.setNombre(nombre);
        cliente.setDni(dni);
        cliente.setDireccion(direccion);
        cliente.setTelefono(telefono);
        cliente.setFechaInstalacion(fechaInstalacion);
        cliente.setTieneMedidor(tieneMedidor);
        cliente.setEstadoServicio(estadoServicio);

        ClienteLogica clienteLogica = new ClienteLogica();
        if (clienteLogica.agregarCliente(cliente)) {
            JOptionPane.showMessageDialog(this, "Cliente registrado con éxito.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos();
            cargarClientesEnTabla();
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar el cliente.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton_registrarActionPerformed

